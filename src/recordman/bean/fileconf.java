package recordman.bean;

public class fileconf {
	private String fault_path=null;
	private String fault_days=null;
	private String continue_path=null;
	private String continue_days=null;
	
	public String getFault_path() {
		return fault_path;
	}
	public void setFault_path(String fault_path) {
		this.fault_path = fault_path;
	}
	public String getFault_days() {
		return fault_days;
	}
	public void setFault_days(String fault_days) {
		this.fault_days = fault_days;
	}
	public String getContinue_path() {
		return continue_path;
	}
	public void setContinue_path(String continue_path) {
		this.continue_path = continue_path;
	}
	public String getContinue_days() {
		return continue_days;
	}
	public void setContinue_days(String continue_days) {
		this.continue_days = continue_days;
	}
	
	
}
